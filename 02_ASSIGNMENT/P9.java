public class P9 {
    public static void main(String[] args) {
        int number=5;


       for(int i=1;i<=number;i++){

            for(int j=number;j>=i;j--){


                    System.out.print(" ");
           
            }
            for(int j=1;j<=i;j++){
                    System.out.print(i +" ");
            }

            System.out.println();

       }

       
    }
}
