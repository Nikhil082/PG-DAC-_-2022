public class P18 {
    public static void main(String[] args) {
        
        int number=5;

        for(int i=number;i>=1;i--){

            for(int j=i;j>=1;j--){

                System.out.print(" ");

            }

            for(int j=i;j<=number;j++){


                System.out.print(j +" ");

            }
            System.out.println();


        }



    }

}   


      
      

      

       

       

       

       
       

       

       
       

       

       




